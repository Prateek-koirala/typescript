class Coder {
    constructor(name, music, age, lang = 'Typescript') {
        this.name = name;
        this.music = music;
        this.age = age;
        this.lang = lang;
        this.name = name;
        this.music = music;
        this.age = age;
        this.lang = lang;
    }
    getAge() {
        return `Hello,I'm ${this.age}`;
    }
}
const Prateek = new Coder('Prateek', 'Rock', 23);
console.log(Prateek.getAge());
